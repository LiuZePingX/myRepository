message = "hello python world"
print(message)

message = "你好，python"
print(message)

# 制表符
print("\thi,python")

# 换行符
print("hi,\npython")

#删除末尾空格
mess = "nihaoya     "
print(mess)
mess = mess.rstrip()
print(mess)

# 删除开头空格
m = "   nihaoyao"
print(m)
m = m.lstrip()
print(m)

# 删除两端空格
me = "   nihaoyao    "
print(me)
me = me.strip()
print(me)

